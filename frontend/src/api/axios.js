import axios from 'axios';

/**
 * Pre-configured Axios instance for the ContIQ API.
 * Automatically attaches the stored JWT to every request.
 */
const api = axios.create({
  baseURL: 'http://localhost:5555'
});

// Request interceptor — attach token from localStorage to every request
api.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => Promise.reject(error)
);

// Response interceptor — clear token and redirect on 401
api.interceptors.response.use(
  (response) => response,
  (error) => {
    if (error.response?.status === 401) {
      localStorage.removeItem('token');
      localStorage.removeItem('user');
      window.location.href = '/login';
    }
    return Promise.reject(error);
  }
);

export default api;
